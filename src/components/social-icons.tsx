const ICON_MAP: Record<string, (props: IconProps) => JSX.Element> = {
  twitter:   TwitterIcon,
  instagram: InstagramIcon,
  tiktok:    TikTokIcon,
  youtube:   YouTubeIcon,
  discord:   DiscordIcon,
  reddit:    RedditIcon,
  facebook:  FacebookIcon,
  twitch:    TwitchIcon,
}

export function SocialIcon({ platform, size = 20, className }: { platform: string } & IconProps) {
  const Icon = ICON_MAP[platform]
  if (!Icon) return null
  return <Icon size={size} className={className} />
}
